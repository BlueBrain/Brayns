import brayns

from .camera_test_case import CameraTestCase


class TestPerspectiveCamera(CameraTestCase):

    def test_all(self) -> None:
        camera = brayns.PerspectiveCamera(
            fovy=brayns.Fovy(30, degrees=True),
            aperture_radius=1,
            focus_distance=2
        )
        self.run_tests(camera)
